    {
        $take = $request->query('limit', 10);
        $skip =  $request->query('offset', 0);
        $page =  $request->query('page', 1);

        $pizzas= DB::table("pizzas")
            ->take($take)
            ->skip($skip + (($page - 1) * $take))
            ->get();
        return $pizzas;
    }

